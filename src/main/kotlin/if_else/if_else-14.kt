package if_else

fun main() {
    val num = 30
    if (num < 10) {
        println("num is smaller than 10")
    } else if (num < 20) {
        println("num is smaller than 20")
    } else if (num < 30) {
        println("num is smaller than 30")
    } else if (num < 40) {
        println("num is smaller than 40")
    }else
        println("num is not big")
}
